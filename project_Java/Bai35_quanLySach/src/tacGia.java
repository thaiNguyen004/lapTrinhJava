
public class tacGia {
	private String ten;
	private Ngay ngaySinh;
	public tacGia(String ten, Ngay ngaySinh) {
		this.ten = ten;
		this.ngaySinh = ngaySinh;
	}
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}
	public Ngay getNgaySinh() {
		return ngaySinh;
	}
	public void setNgaySinh(Ngay ngaySinh) {
		this.ngaySinh = ngaySinh;
	}
	
	
}
